public class Calc {
    public static void main(String[] args) {
        int a = 15, b = 10;
        int add = a+b;
        int diff = a-b;
        int prod = a*b;
        int div = a/b;
        int mod = a%b;
        System.out.println(add);
        System.out.println(diff);
        System.out.println(prod);
        System.out.println(div);
        System.out.println(mod);

        float g = 10.0f, f = 2.5f;
        float res = g/f;
        System.out.println(res);

    }
    
}
